
public class DataTypes {

	public static void main(String[] args) {
		// byte
		byte b1=25;
		byte b2=-128;
		byte b3=127;
		System.out.println(b1+"|"+b2+"|"+b3);
		
		//short
		short s1=500;
		short s2=32767;
		
		//int
		int i1=-50000;
		int i2=100_0000;
		
		//long
		long l1=100_000_000;
		long l2=123242535646754l;
		
		//float
		float f1=5.2f;
		System.out.println(f1);
		
		//boolean
		
		boolean bool=true;
		boolean bool2=false;
		System.out.println(bool+"|"+ bool2);
		
		//char
		char c1='a';
		char c2='2';
		char c3='@';
		System.out.println(c1+"|"+c2+"|"+c3);
		

	}

}
