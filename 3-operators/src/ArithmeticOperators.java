
public class ArithmeticOperators {

	public static void main(String[] args) {
		int i1=10;
		int i2=5;
		
		float f1=12.5f;
		double d1=98.4;
		
		String msg="hello";
		
		int res=i1+i2;
		float res2=i2+f1;
		double res3=f1+d1;
		String res4=msg+f1;
		
		System.out.println(res);
		System.out.println(res2);
		System.out.println(res3);
		System.out.println(res4);
		

	}

}
